//进制
public class GenericBinary {

    public static void main(String[] args){

        System.out.println("123的十进制为："+Integer.toString(123));
        System.out.println("123的二进制为："+Integer.toBinaryString(123));
        System.out.println("123的十六进制为："+Integer.toHexString(123));
        System.out.println("123的八进制为："+Integer.toOctalString(123));

    }




}
